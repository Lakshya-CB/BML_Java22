package Lec_6;

public class char_demo {
	public static void main(String[] args) {
		char ch = 'c';
		System.out.println(ch+1);
		int nn = ch+100;
		
		int val = (int)ch;
		System.out.println(val);
		System.out.println((char)nn);
		
		System.out.println((int)'a');
		System.out.println((int)'A');
		System.out.println('a'+'b');
		System.out.println('a'-'b');
		
		
	}
}
